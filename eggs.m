function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
c = get(handles.slider1,'value');
d = get(handles.slider2,'value');
sys = readfis('Boiled Egg.fis');
x = evalfis([c,d],sys)
set(handles.edit1,'String',num2str(x));
if(x>0 && x<39)
     set(handles.edit1,'String','RAW eggs');
else if(x>38 && x<64)
     set(handles.edit1,'String','Shirred eggs');
else if(x>63 && x<87)
     set(handles.edit1,'String','Hard-boiled eggs');
end
end
end
